valor1 = 4
valor2 = 2

soma = valor1 + valor2
print(f"Soma: {soma}") # resultado: 6

resto = valor1 - valor2
print(resto) # resultado: 2

produto = valor1 * valor2
print(produto) # resultado : 8

quociente_exato = valor1 / valor2
print(quociente_exato) # res: 2.0

quociente_inteiro = valor1 // valor2
print(quociente_inteiro) # res: 2

modulo = valor1 % valor2
print(modulo) # resultado: 0

potencia = valor1 ** valor2
print(potencia) # resultado: 16

